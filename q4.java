// increment & decrement operator 
public class SumOfNumbers1  
{  
public static void main(String args[])   
{  
    int i = 56;
    System.out.println(i++);
    System.out.println(i);
    System.out.println(++i);
    System.out.println(i);

    //decrement 
    System.out.println(i--);
    System.out.println(i);
    System.out.println(--i);
    System.out.println(i);
}  
}  